#include "libft.h"

int			ft_ch_in_nb_long(long long nb)
{
	int		l;

	if (!(l = 0) && nb == LONG_MIN && (l += 2) != -1)
		nb = 223372036854775808;
	if (nb < 0 && l++ != -1)
		nb *= -1;
	if (nb > 9)
		l += ft_ch_in_nb_long(nb / 10);
	return (++l);
}
